export type AppLocale = "zh-CN" | "en";
export type DisplayLanguageSetting = "auto" | AppLocale;

export function normalizeLocale(input?: string): AppLocale {
	if (!input) return "en";
	const value = input.toLowerCase();
	if (value.startsWith("zh")) return "zh-CN";
	return "en";
}

export function resolveDisplayLocale(configured: DisplayLanguageSetting, envLanguage: string): AppLocale {
	if (configured === "auto") {
		return normalizeLocale(envLanguage);
	}
	return configured;
}


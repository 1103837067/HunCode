export type MessageKey = keyof (typeof messages)["en"];

export function t(locale: AppLocale, key: MessageKey, arg?: number): string {
	const dict = locale === "zh-CN" ? messages["zh-CN"] : messages.en;
	const entry = dict[key];
	if (typeof entry === "function") {
		return entry(arg ?? 0);
	}
	return entry;
}
